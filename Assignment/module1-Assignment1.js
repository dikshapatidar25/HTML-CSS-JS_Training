// 1. Output your full name
console.log("Diksha Patidar");

// 2. Output your year of birth
console.log("2003");

// 3. Output year of birth without quotes (as a number)
console.log(2003);

// 4. Multiple arguments
// a.
console.log("abc", "def", "ghi");

// b. Information about yourself
console.log("Diksha Patidar (2003)");

// c. All information as one argument
console.log("Diksha Patidar (2003)");

// d. Name, surname, and year as separate arguments
console.log("Diksha", "Patidar", "(2003)");

// 5. Output on consecutive lines
console.log("Diksha");
console.log("Patidar");
console.log("2003");

// 6. Concatenate with +
console.log("Diksha" + " " + "Patidar" + " " + "(2003)");

// 7. Put spaces correctly using commas
console.log("Diksha", "Patidar", "(2002)");
